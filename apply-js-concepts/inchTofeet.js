function inchToFeet(inches) {
  var feet = inches * 12;
  return feet;
}
var feet = inchToFeet(125);
console.log(feet);

function dadiInches(dadinches) {
  var newfeet = dadinches * 18;
  return newfeet;
}
var dadinches = 256;
var newfeet = dadiInches(dadinches);
console.log(newfeet);

function MileToKilometer(miles) {
  var km = miles * 1.60934;
  return km;
}

var marathon = MileToKilometer(120);
console.log(marathon);
